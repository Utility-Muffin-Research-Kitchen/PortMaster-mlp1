      if leaf_pm_dir_has_font "\$_leaf_pm_java_font_candidate"; then
        _leaf_pm_java_font_dir="\$_leaf_pm_java_font_candidate"
        break
      fi
    done
  fi

  if [ -n "\$_leaf_pm_java_font_dir" ] && [ -d "\$_leaf_pm_java_font_dir" ]; then
    case " \${JAVA_TOOL_OPTIONS:-} " in
      *" -Dsun.java2d.fontpath="*) ;;
      *) export JAVA_TOOL_OPTIONS="-Dsun.java2d.fontpath=\$_leaf_pm_java_font_dir\${JAVA_TOOL_OPTIONS:+ \$JAVA_TOOL_OPTIONS}" ;;
    esac
  fi

  if [ -z "\${CRUSTY_RESOLUTION:-}" ]; then
    export CRUSTY_RESOLUTION="\${LEAF_PM_JAVA8_WESTON_CRUSTY_RESOLUTION:-\${DISPLAY_WIDTH:-960}x\${DISPLAY_HEIGHT:-720}}"
  fi

  if declare -f leaf_pm_enable_java8_weston_direct_drm >/dev/null 2>&1; then
    leaf_pm_enable_java8_weston_direct_drm || true
  fi

  unset _leaf_pm_java_font_candidate _leaf_pm_java_font_dir
}

export LEAF_PM_EGL_SHIM_DIR="\${LEAF_PM_EGL_SHIM_DIR:-\$LEAF_PM_DATA_DIR/compat/egl/aarch64}"
export LEAF_PM_MALI_AARCH64_DIR="\${LEAF_PM_MALI_AARCH64_DIR:-\$LEAF_PM_DATA_DIR/compat/mali/aarch64}"
leaf_pm_prepare_godot_runtime_env() {
  [ "\${DEVICE_ARCH:-aarch64}" = "aarch64" ] || return 0
  export LEAF_PM_SKIP_WESTONPACK_CLEANUP="\${LEAF_PM_SKIP_WESTONPACK_CLEANUP:-1}"
  # The stock Mali blob mis-tracks in-flight GPU jobs against Godot 4's
  # streamed canvas buffers: the kernel logs "Unhandled Page fault" / job
  # TERMINATED every frame and the screen stays black after the boot splash.
  # The Leaf EGL shim serializes draws to work around it (1 = glFinish after
  # each draw). Override with LEAF_PM_GODOT_DRAW_FINISH=0 to disable.
  export LEAF_EGL_DRAW_FINISH="\${LEAF_PM_GODOT_DRAW_FINISH:-\${LEAF_EGL_DRAW_FINISH:-1}}"
  _leaf_pm_godot_cmd="\${1:-}"
  _leaf_pm_godot_use_mali_default=0
  _leaf_pm_godot_use_mali="\${LEAF_PM_GODOT_USE_MALI_COMPAT:-\$_leaf_pm_godot_use_mali_default}"
  _leaf_pm_wayland_runtime="\${LEAF_PM_WAYLAND_RUNTIME_DIR:-\${XDG_RUNTIME_DIR:-/var/run}}"
  _leaf_pm_wayland_display="\${LEAF_PM_WAYLAND_DISPLAY:-\${WAYLAND_DISPLAY:-wayland-0}}"
  _leaf_pm_egl_shim="\${LEAF_PM_EGL_SHIM_DIR:-}/libEGL.so.1"
  case "\$_leaf_pm_godot_use_mali" in
    1|true|yes|TRUE|YES)
      if [ -f "\${LEAF_PM_MALI_AARCH64_DIR:-}/libmali.so.1" ]; then
        export LEAF_PM_MALI_LIB="\$LEAF_PM_MALI_AARCH64_DIR/libmali.so.1"
        case ":\${LD_LIBRARY_PATH:-}:" in
          *:"\$LEAF_PM_MALI_AARCH64_DIR":*) ;;
          *) export LD_LIBRARY_PATH="\$LEAF_PM_MALI_AARCH64_DIR\${LD_LIBRARY_PATH:+:\$LD_LIBRARY_PATH}" ;;
        esac
      fi
      ;;
  esac
  if [ -f "\$_leaf_pm_egl_shim" ]; then
    case ":\${LD_LIBRARY_PATH:-}:" in
      *:"\$LEAF_PM_EGL_SHIM_DIR":*) ;;
      *) export LD_LIBRARY_PATH="\$LEAF_PM_EGL_SHIM_DIR\${LD_LIBRARY_PATH:+:\$LD_LIBRARY_PATH}" ;;
    esac
  fi
  export XDG_RUNTIME_DIR="\$_leaf_pm_wayland_runtime"
  export WAYLAND_DISPLAY="\$_leaf_pm_wayland_display"
  export SDL_VIDEODRIVER=wayland
}

leaf_pm_args_have_display_driver() {
  while [ "\$#" -gt 0 ]; do
    case "\$1" in
      --display-driver|--display-driver=*) return 0 ;;
    esac
    shift
  done
  return 1
}

leaf_pm_args_have_window_mode() {
  while [ "\$#" -gt 0 ]; do
    case "\$1" in
      -f|--fullscreen|--fullscreen=*|-w|--windowed|--windowed=*|--resolution|--resolution=*|--position|--position=*) return 0 ;;
    esac
    shift
  done
  return 1
}

leaf_pm_run_godot_wayland_runtime() {
  [ "\$#" -gt 0 ] || return 127
  _leaf_pm_godot_cmd="\$1"
  shift
  leaf_pm_prepare_godot_runtime_env "\$_leaf_pm_godot_cmd"
  if leaf_pm_args_have_display_driver "\$@"; then
    "\$_leaf_pm_godot_cmd" "\$@"
    return \$?
  fi
  "\$_leaf_pm_godot_cmd" --display-driver wayland "\$@"
}

leaf_pm_run_godot_sdl2_runtime() {
  [ "\$#" -gt 0 ] || return 127
  _leaf_pm_godot_cmd="\$1"
  shift
  leaf_pm_prepare_godot_runtime_env "\$_leaf_pm_godot_cmd"
  _leaf_pm_direct_resolution="\${LEAF_PM_GODOT_DIRECT_RESOLUTION:-960x720}"
  if [ -n "\$_leaf_pm_direct_resolution" ] && ! leaf_pm_args_have_window_mode "\$@"; then
    "\$_leaf_pm_godot_cmd" --resolution "\$_leaf_pm_direct_resolution" "\$@"
    return \$?
  fi
  "\$_leaf_pm_godot_cmd" "\$@"
}

leaf_pm_enable_godot_wayland_runtime() {
  _leaf_pm_env_bin="\$(command -v env 2>/dev/null || printf '%s\n' /usr/bin/env)"
  env() {
    if [ "\${1##*/}" = "westonwrap.sh" ] && [ "\${2:-}" != "cleanup" ] && [ "\$#" -ge 6 ]; then
      # MLP1 already has Leaf's Weston running; PortMaster's nested Westonpack
      # wrapper cannot provide the EGL display Godot 4 expects on stock firmware.
      shift
      shift 4
      while [ "\$#" -gt 0 ]; do
        case "\$1" in
          *=*) export "\$1"; shift ;;
          *) break ;;
        esac
      done
      [ "\$#" -gt 0 ] || return 127
      _leaf_pm_godot_cmd="\$1"
      shift
      leaf_pm_run_godot_wayland_runtime "\$_leaf_pm_godot_cmd" "\$@"
      return \$?
    fi
    "\$_leaf_pm_env_bin" "\$@"
  }
  if command -v sudo >/dev/null 2>&1; then
    sudo() {
      if [ "\${1:-}" = "env" ] && [ "\${2##*/}" = "westonwrap.sh" ]; then
        shift
        env "\$@"
        return \$?
      fi
      if [ "\${2:-}" = "env" ] && [ "\${3##*/}" = "westonwrap.sh" ]; then
        shift 2
        env "\$@"
        return \$?
      fi
      if [ "\${3:-}" = "env" ] && [ "\${4##*/}" = "westonwrap.sh" ]; then
        shift 3
        env "\$@"
        return \$?
      fi
      command sudo "\$@"
    }
  fi
}

leaf_pm_enable_egl_gles_shim() {
