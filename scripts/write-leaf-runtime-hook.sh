leaf_pm_dir_has_font() {
  [ -d "\${1:-}" ] || return 1
  for _leaf_pm_font in "\$1"/*.ttf "\$1"/*.otf "\$1"/*.ttc; do
    [ -f "\$_leaf_pm_font" ] && unset _leaf_pm_font && return 0
  done
  unset _leaf_pm_font
  return 1
}

