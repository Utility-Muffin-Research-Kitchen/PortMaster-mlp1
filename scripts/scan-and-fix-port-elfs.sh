# Generated by PortMaster-mlp1. Safe to overwrite.

_leaf_pm_controlfolder="\${controlfolder:-\${PORTMASTER_CONTROLFOLDER:-$controlfolder}}"
if [ -n "\$_leaf_pm_controlfolder" ] && [ -d "\$_leaf_pm_controlfolder/.." ]; then
  _leaf_pm_data_dir="\$(CDPATH= cd -- "\$_leaf_pm_controlfolder/.." && pwd)"
else
  _leaf_pm_data_dir="$data_dir"
fi
